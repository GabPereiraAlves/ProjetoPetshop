    private void listarAgendamentos(){
        try {
            AgendamentoDAO objagendamentodao = new AgendamentoDAO();
            DefaultTableModel model = (DefaultTableModel) tblAgendamentos.getModel();
            model.setNumRows(0);
            
            ArrayList<AgendamentoDTO> lista = objagendamentodao.listarAgendamentos();
            
            for(int i = 0; i < lista.size();i++){
                ids_agendamentos[i] = lista.get(i).getId_agendamento();
                model.addRow(new Object[]{
                    lista.get(i).getNomePet(),
                    lista.get(i).getEspecie(),
                    lista.get(i).getPorte(),
                    lista.get(i).getPeso(),
                    lista.get(i).getData(),
                    lista.get(i).getHorario(),
                    lista.get(i).getValorServico(),
                    lista.get(i).getNomeProfissional(),
                    lista.get(i).getProcediemnto(),
                    lista.get(i).getRealizado(),
                });
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ListarAgendamentos: " + erro);
        }
    }
    
    private void alterarAgendamento(){
        int id_agendamento, linha;
        String nomepet, especie, porte, peso, data, horario, valor, profissional,
                procedimento, realizado;
        
        AgendamentoDTO objagendamentodto = new AgendamentoDTO();
        
        linha = tblAgendamentos.getSelectedRow();
        
        id_agendamento = ids_agendamentos[linha];
        objagendamentodto.setId_agendamento(id_agendamento);
        
        nomepet = tblAgendamentos.getModel().getValueAt(linha, 0).toString();
        objagendamentodto.setNomePet(nomepet);
        
        especie = tblAgendamentos.getModel().getValueAt(linha, 1).toString();
        objagendamentodto.setEspecie(especie);
        
        porte = tblAgendamentos.getModel().getValueAt(linha, 2).toString();
        objagendamentodto.setPorte(porte);
        
        peso = tblAgendamentos.getModel().getValueAt(linha, 3).toString();
        objagendamentodto.setPeso(peso);
        
        data = tblAgendamentos.getModel().getValueAt(linha, 4).toString();
        objagendamentodto.setData(data);
        
        horario = tblAgendamentos.getModel().getValueAt(linha, 5).toString();
        objagendamentodto.setHorario(horario);
        
        valor = tblAgendamentos.getModel().getValueAt(linha, 6).toString();
        objagendamentodto.setValorServico(valor);
        
        profissional = tblAgendamentos.getModel().getValueAt(linha, 7).toString();
        objagendamentodto.setNomeProfissional(profissional);
        
        procedimento = tblAgendamentos.getModel().getValueAt(linha, 8).toString();
        objagendamentodto.setProcediemnto(procedimento);
        
        realizado = tblAgendamentos.getModel().getValueAt(linha, 9).toString();
        objagendamentodto.setRealizado(realizado);
        
        AgendamentoDAO objagendamentodao = new AgendamentoDAO();
        objagendamentodao.alterarAgendamento(objagendamentodto);
    }
    
    private void deletaAgendamento(){
        int linha, id_agendamento;
        String idString;
        
        linha = tblAgendamentos.getSelectedRow();
        
        id_agendamento = ids_agendamentos[linha];
        
        AgendamentoDTO objagendamentodto = new AgendamentoDTO();
        objagendamentodto.setId_agendamento(id_agendamento);
        
        AgendamentoDAO objagendamentodao = new AgendamentoDAO();
        objagendamentodao.deletaAgendamento(objagendamentodto);
    }
